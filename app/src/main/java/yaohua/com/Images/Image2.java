package yaohua.com.Images;

public class Image2 {

    private String title2, detail2, sells, url2;

    public Image2() {

    }

    public Image2(String title2, String detail2, String sells, String url2) {
        this.title2 = title2;
        this.detail2 = detail2;
        this.sells = sells;
        this.url2 = url2;
    }

    public String getTitle2() {
        return title2;
    }

    public void setTitle2(String title2) {
        this.title2 = title2;
    }

    public String getDetail2() {
        return detail2;
    }

    public void setDetail2(String detail2) {
        this.detail2 = detail2;
    }

    public String getSells() {
        return sells;
    }

    public void setSells(String sells) {
        this.sells = sells;
    }

    public String getUrl2() {
        return url2;
    }

    public void setUrl2(String url2) {
        this.url2 = url2;
    }
}
